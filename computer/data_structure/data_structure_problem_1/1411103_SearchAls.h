/*
* Tên hàm: LinearSearch
* Mô tả: Tìm kiếm tuần tự trên mảng
* Kiểu trả về: int
* Tham số: mảng nguyên a.
* Tham số: số nguyên n, chiều dài của mảng.
* Tham số: số nguyên x cần tìm kiếm trong mảng a.
*/
int LinearSearch(int a[], int& n, int& x);

/*
* Tên hàm: BinarySearch
* Mô tả: Tìm kiếm nhị phân trên mảng
* Kiểu trả về: int
* Tham số: mảng nguyên a.
* Tham số: số nguyên n, chiều dài của mảng.
* Tham số: số nguyên x cần tìm kiếm trong mảng a.
*/
int BinarySearch(int a[], int& n, int& x);