#include <iostream>
#include <cmath>
using namespace std;
int main() {
    cout << "sqrt(9): " << sqrt(9) << endl;
    cout << "pow(2, 3): " << pow(2, 3) << endl;
    cout << "exp(1): " << exp(1) << endl;
    cout << "log(2.718): " << log(2.71828) << endl;
    cout << "log10(100): " << log10(100) << endl;
    cout << "abs(-5): " << abs(-5) << endl;
    cout << "labs(-1000000): " << labs(-1000000) << endl;
    cout << "fabs(-5.5): " << fabs(-5.5) << endl;
    cout << "cos(0): " << cos(0) << endl;
    cout << "sin(0): " << sin(0) << endl;
    cout << "tan(0): " << tan(0) << endl;
    cout << "acos(1): " << acos(1) << endl;
    cout << "asin(0): " << asin(0) << endl;
    cout << "atan(0): " << atan(0) << endl;
    cout << "floor(3.8): " << floor(3.8) << endl;
    cout << "ceil(3.2): " << ceil(3.2) << endl;
    return 0;
}